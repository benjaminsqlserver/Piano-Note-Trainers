const DEGREE_NAME = { I: 'Tonic', ii: 'Supertonic', iii: 'Mediant', IV: 'Subdominant', V: 'Dominant', vi: 'Submediant', 'vii°': 'Leading tone' };
const TICKS_PER_QUARTER = 192; // this hymn's own tick grid (6/8 time)
const BASE_MS_PER_TICK = (60000 / 100) / TICKS_PER_QUARTER; // the source MIDI file is authored at 100 BPM

/** Builds the note names for one chord chart entry in `key`, for whichever quality it is. */
function chordNoteNames(entry, key) {
  const intervals = QUALITY_INTERVALS[entry.quality];
  const rootSemitone = key.semitoneFromC + entry.semitoneFromTonic;
  return intervals.map((semitone) => noteNameFor(rootSemitone + semitone));
}

