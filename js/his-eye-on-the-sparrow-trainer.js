// "His Eye Is on the Sparrow" (words: Civilla D. Martin, 1905; music:
// Charles H. Gabriel, 1905 — public domain) is kept EXACTLY as arranged in
// the source MIDI file (from the Cyber Hymnal, via NoteWorthy Composer) --
// nothing about the arrangement itself was changed, only transposed into
// each of the 12 keys:
//   - MIDI channel 0 (right hand): the hymn's melody line.
//   - MIDI channel 1 (left hand): the hymn's own accompaniment line.
// The chart below is a harmonic analysis of that unaltered arrangement --
// every distinct chord the two hands sound together, in playback order --
// generated by combining both hands' notes at each moment and matching the
// resulting pitch-class set against known chord shapes. Most of it is
// ordinary major and minor triads (I, ii, iii, IV, V, vi), but several
// passages use 7th chords this app now teaches as their own lessons: the
// dominant 7th (mostly V7 — see the Dominant 7th Chord lesson), the
// fully-diminished 7th (a chromatic passing chord — see the Diminished
// 7th Chord lesson), the minor 7th (ii7/iii7 — see the Minor 7th Chord
// lesson), the major 7th (a tonic color chord — see the Major 7th Chord
// lesson), and the half-diminished 7th (the scale's own diatonic
// leading-tone 7th chord, viiø7 — see the Half-Diminished 7th Chord
// lesson). semitoneFromTonic + quality describe each chord in a key-independent way, exactly like the Power in the Blood
// Song Trainer's SONG_CHORDS does, using this file's own tick grid (192
// ticks per quarter note, 6/8 time).
const SONG_KEYS = ['C', 'C#', 'D', 'D#', 'E', 'F', 'F#', 'G', 'G#', 'A', 'A#', 'B'].map((name, semitoneFromC) => ({
  semitoneFromC,
  name,
  fileSlug: name.replace('#', 's'),
}));

