/** Builds the note names for one chord chart entry in `key`, for whichever quality it is, honoring the mid-song key change via entry.keyOffset. */
function chordNoteNames(entry, key) {
  const intervals = QUALITY_INTERVALS[entry.quality];
  const rootSemitone = key.semitoneFromC + entry.keyOffset + entry.semitoneFromTonic;
  return intervals.map((semitone) => noteNameFor(rootSemitone + semitone));
}

